# Multiplication in F8
def mul(a, b):
    result = 0
    for i in range(8):
        if b & 1 == 1:
            result ^= a
        bit = a & 0x80
        a <<= 1
        if bit == 0x80:
            a ^= 0x1b
        b >>= 1
    return result % 256


