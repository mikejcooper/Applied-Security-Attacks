# Convert to Hex string
def toHex(X):
    if isinstance(X, ( int, long )):
        return "%X\n" % X
    elif X == '':
        return 0
    else:
        return X.encode('hex')

# Multiply a(x) by x
def gf28_mulx(a):
    return (((a << 1) ^ 0x1B) if a & 0x80 else (a << 1)) & 0xFF

# Multiply a(x) by b(x)
def gf28_mul(a, b):
    t = 0
    for i in range(7, -1, -1):
        t = gf28_mulx(t)
        if (b >> i) & 1:
            t ^= a
    return t


