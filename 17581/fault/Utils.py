def AES_check( m, c, k ) :
    c1 = AES.new( HexToByte(k) ).encrypt( HexToByte(m) )
    if HexToByte(c) == HexToByte(c1):
        return True
    else :
        return False

def AES_1_Block(text):
    return hashlib.md5(text).digest()

